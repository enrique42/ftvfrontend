					<h3>Temas</h3>
					<ul>
						<li>
							<h4>Autoformação & Novas Formas de Aprendizagem</h4>
							<p>Novos modelos educacionais e tecnologias que valorizam o desenvolvimento transdisciplinar, prático, experimental, dinâmico, virtual, gamificado, informal (não curriculuar), não hierárquico: de professor>aluno para horizontal (entre pares) ou reverso (dos menos experientes para os mais experientes), continuado, acessando conteúdo ilimitado e multicultural.</p>
						</li>
						<li>
							<h4>Ativismo e representação</h4>
							<p>Indíviduos e grupos mais informados, conectados e ativos socialmente se articulam e manifestam em prol de causas (políticas, econômicas, sociais, meio ambiente) e respostas de seus governos ou até visando derrubar governos que não correspondem aos anseios da sociedade. Governos buscando mecanismos de diálogo para lidar com a crise de representação.</p>
						</li>
						<li>
							<h4>Novas Fronteiras Legais</h4>
							<p>Repensar dos direitos e deveres de indivíduos, governos e empresas na era digital e da era digital. Analisar a capacidade do legislativo em acompanhar movimentos que emergem na sociedade.</p>
						</li>
						<li>
							<h4>Life Tracking</h4>
							<p>Estilo de vida saudável e induzido por novos medicamentos e tecnologias permitem aumentar significativamente o tempo de vida. Plataformas inteligentes que permitem monitoramento (preventivo) e contínuo da saúde, autodiagnostico/exames com indicação de tratamentos e mudanças e comportamento.</p>
						</li>
						<li>
							<h4>Pluralidade/Diversidade</h4>
							<p>Revisão e enfrentamento de valores, crenças e símbolos de diferentes culturas e polaridades/paradoxos presentes na sociedade: global x local, secular x contemporâneo, low tech x hi tech, materialista x espiritualista, entre outros.</p>
						</li>
						<li>
							<h4>Novas formas de trabalho e carreira</h4>
							<p>Mudanças no estilo e ambiente de trabalho (+ flexível, virtual, fragmentado e orientado a múltiplas carreiras) e relacionamento entre profissionais e as instituições que os contratam (pessoas trabalhando com as empresas e não mais para as empresas).</p>
						</li>
						<li>
							<h4>Consciência e Bem Estar </h4>
							<p>Na saúde e bem estar, maior conscientização com mudanças de hábitos a atitudes cotidianas de autoindulgência. Alterações nos padrões de consumo e busca por mais informação do impacto da compra.</p>
						</li>
						<li>
							<h4>Hiperconexão</h4>
							<p>Aperfeiçoamento de sensores wireless com uso de nanotecnologia conectando objetos/aparelhos do dia-a-dia das pessoas a grandes bases de dados e à Internet, transformando objetos/aparelhos estáticos em algo dinâmico, misturando inteligência ao meio, com alcance e impacto sobre a vida das pessoas ainda desconhecido. Desaparece a diferença entre o online e o offline.</p>
						</li>
						<li>
							<h4>Reconfiguração das cidades</h4>
							<p>Reconstrução das cidades: cidade como digital, cidade da informação, 'cidade conectada’, cidade baseada no conhecimento, comunidade eletrônica, cobrindo uma ampla gama de aplicações eletrônicas e digitais. Territórios caracterizados pela alta capacidade de aprendizado e inovação, que já é embutida na criatividade de sua população, suas instituições de geração de conhecimento, e sua infra-estrututura digital para comunicação e gestão do conhecimento.</p>
						</li>
						<li>
							<h4>Inteligencia artificial</h4>
							<p> Assim como as máquinas superaram a força física humana, o raciocínio lógico humano também começa a ser replicado e, eventualmente, superado por dispositivos e softwares. Na medicina, pode ganhar relevância, considerando que o cruzamento de um grande volume de dados irá facilitar processos de diagnóstico e a cura de doenças.</p>
						</li>
						<li>
							<h4>Produção descentralizada</h4>
							<p>Democratização do acesso a tecnologias que permitem o design, prototipagem rápida e produção de artefatos em menor escala para diferentes fins tais como manufatura, construção civil, medicina regenerativa e biogenética. Soluções eficazes, baratas e acessíveis contribuem no enfrentamento de problemas sociais como acesso a moradia, por exemplo. </p>
						</li>
						<li>
							<h4>Economia de recursos</h4>
							<p>Constatação de que os recursos físicos do planeta não são abundantes mas limitados para demandas crescentes da economia e da sociedade e consequente tomada de consciência com ações preventivas e corretivas para reduzir ou rever o padrão de consumo e “pegada” do ser humano no meio ambiente.</p>
						</li>
						<li>
							<h4>Consumo compartilhado</h4>
							<p>Novos modelos/conceitos econômicos: moedas (sociais, digitais), compartilhamento do acesso à propriedade privada (ex: carro, casa), acesso (a a produtos/serviços (“streaming”, “everything as a servisse”) ao invés da compra, crowdfunding e outros mecanismos de microfinanças.</p>
						</li>
						<li>
							<h4>Convergencia tecnológica</h4>
							<p>Movimento da transdisciplinariedade científica e tecnológica, tendo como principais protagonistas a neurosciência, nanotecnologia, biotecnologia e inteligência artificial, em busca de soluções de impacto para os problemas da humanidade.</p>
						</li>
