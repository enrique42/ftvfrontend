							<h4>Inovação no Terceiro Setor</h4>
							<p>Repensar do investimento social privado – governança, cultura de doação e investimento social familia na perspectiva do impacto. Crescimento de negócios sociais e avanços nos modelos de gestão e financianmento  terceiro setor.</p>
